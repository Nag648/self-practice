
// Example of a for loop that iterates from 1 to 5
for (let i = 1; i <= 5; i++) {
    console.log("Iteration:", i);
  }

  
  // Example of a while loop that iterates as long as a condition is true
let counter = 1;
while (counter <= 5) {
  console.log("Iteration:", counter);
  counter++;
}



// Example of a do-while loop that executes at least once and continues as long as a condition is true
let number = 1;
do {
  console.log("Number:", number);
  number++;
} while (number <= 5);




